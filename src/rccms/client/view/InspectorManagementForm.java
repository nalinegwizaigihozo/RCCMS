    private boolean validateInputs() {
    if (idNumberField.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Inspector ID number is required!",
                "Validation Error", JOptionPane.WARNING_MESSAGE);
        idNumberField.requestFocus();
        return false;
    }
    if (firstNameField.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "First name is required!",
                "Validation Error", JOptionPane.WARNING_MESSAGE);
        firstNameField.requestFocus();
        return false;
    }
    if (lastNameField.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Last name is required!",
                "Validation Error", JOptionPane.WARNING_MESSAGE);
        lastNameField.requestFocus();
        return false;
    }
    if (emailField.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Email is required!",
                "Validation Error", JOptionPane.WARNING_MESSAGE);
        emailField.requestFocus();
        return false;
    }
    
    // ADD PHONE VALIDATION HERE
    if (phoneField.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Phone number is required!",
                "Validation Error", JOptionPane.WARNING_MESSAGE);
        phoneField.requestFocus();
        return false;
    }
    
    if (!isValidRwandanPhone(phoneField.getText().trim())) {
        JOptionPane.showMessageDialog(this, 
                "Invalid phone number!\n" +
                "Rwandan phone numbers must be 10 digits starting with:\n" +
                "ÃƒÂ¢Ã¢â€šÂ¬Ã‚Â¢ MTN: 078 or 079\n" +
                "ÃƒÂ¢Ã¢â€šÂ¬Ã‚Â¢ Airtel: 073 or 072\n" +
                "Example: 0781234567 or 0733456789",
                "Validation Error", JOptionPane.WARNING_MESSAGE);
        phoneField.requestFocus();
        return false;
    }
    
    if (districtField.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Assigned district is required!",
                "Validation Error", JOptionPane.WARNING_MESSAGE);
        districtField.requestFocus();
        return false;
    }
    return true;
}

