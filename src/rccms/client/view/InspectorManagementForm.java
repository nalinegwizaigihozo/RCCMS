private boolean isValidRwandanPhone(String phone) {
    // Remove any spaces, dashes, or parentheses
    phone = phone.replaceAll("[\\s\\-()]", "");
    
    // Remove country code if present
    if (phone.startsWith("+250")) {
        phone = "0" + phone.substring(4);
    } else if (phone.startsWith("250")) {
        phone = "0" + phone.substring(3);
    }
    
    // Check if it's exactly 10 digits starting with 07
    if (!phone.matches("^07\\d{8}$")) {
        return false;
    }
    
    // Extract the prefix (first 3 digits after 0)
    String prefix = phone.substring(0, 3);
    
    // Valid Rwandan mobile prefixes
    // MTN: 078, 079
    // Airtel: 073, 072
    return prefix.equals("078") ||  // MTN
           prefix.equals("079") ||  // MTN
           prefix.equals("073") ||  // Airtel
           prefix.equals("072");    // Airtel/Tigo
}

