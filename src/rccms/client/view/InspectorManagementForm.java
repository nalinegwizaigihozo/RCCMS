     private void loadInspectors() {
        try {
            tableModel.setRowCount(0);
            inspectorService = rccms.client.util.RMIClientUtil.getInspectorService();
            if (inspectorService != null) {
                List<Inspector> inspectors = inspectorService.findAll();
                for (Inspector inspector : inspectors) {
                    Object[] row = {
                            inspector.getInspectorId(),
                            inspector.getInspectorIdNumber(),
                            inspector.getFirstName(),
                            inspector.getLastName(),
                            inspector.getEmail(),
                            inspector.getPhone(),
                            inspector.getQualifications(),
                            inspector.getAssignedDistrict(),
                            inspector.getEmploymentStatus()
                    };
                    tableModel.addRow(row);
                }
            } else {
                 JOptionPane.showMessageDialog(this, "Could not connect to server.", "Connection Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Error loading inspectors: " + e.getMessage(),
                    "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }

