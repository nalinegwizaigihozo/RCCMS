// Refresh table with all data
    private void refreshTable() {
        tableModel.setRowCount(0);
        List<Church> churches = null;
        try {
             if (churchService == null) churchService = rccms.client.util.RMIClientUtil.getChurchService();
             churches = churchService.findAll();
        } catch (Exception e) {
             e.printStackTrace();
             churches = java.util.Collections.emptyList();
        }
        for (Church church : churches) {
            Object[] row = {
                church.getChurchId(),
                church.getChurchName(),
                church.getRegistrationNumber(),
                church.getPastorName(),
                church.getPastorPhone(),
                church.getPastorEmail(),
                church.getAddress(),
                church.getDistrict(),
                church.getSector(),
                church.getCell(),
                church.getCapacity(),
                church.getBuildingType(),
                church.getComplianceStatus(),
                church.getRegistrationDate(),
                church.getLastInspectionDate()
            };
            tableModel.addRow(row);
        }


    }//GEN-LAST:event_btnsearchActionPerformed

