    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (!validateInputs()) {
            return;
        }

        try {
            Church church = new Church();
            church.setChurchName(txtChurchName.getText().trim());
            church.setRegistrationNumber(txtRegistrationNumber.getText().trim());
            church.setPastorName(txtPastorName.getText().trim());
            church.setPastorPhone(txtPastorPhone.getText().trim());
            church.setPastorEmail(txtPastorEmail.getText().trim());
            church.setAddress(txtAddress.getText().trim());
            church.setDistrict(txtDistrict.getText().trim());
            church.setSector(txtSector.getText().trim());
            church.setCell(txtCell.getText().trim());
            church.setCapacity(Integer.parseInt(txtCapacity.getText().trim()));
            church.setBuildingType(cmbBuildingType.getSelectedItem().toString());
            church.setComplianceStatus("PENDING");
            church.setRegistrationDate(new Date());

            if (church.getCapacity() <= 0) {
                JOptionPane.showMessageDialog(this, "Capacity must be greater than 0!",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (churchService.existsByRegistrationNumber(church.getRegistrationNumber())) {
                JOptionPane.showMessageDialog(this, "Registration number already exists!",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (churchService.create(church)) {
                JOptionPane.showMessageDialog(this, "Church added successfully!",
                        "Success", JOptionPane.INFORMATION_MESSAGE);
                loadChurches();
                clearFields();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add church!",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
             e.printStackTrace();
             JOptionPane.showMessageDialog(this, "Error creating church: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

