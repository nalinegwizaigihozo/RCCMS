    private boolean validateInputs() {
        if (txtChurchName.getText().trim().isEmpty()
                || txtRegistrationNumber.getText().trim().isEmpty()
                || txtPastorName.getText().trim().isEmpty()
                || txtPastorEmail.getText().trim().isEmpty()
                || txtPastorPhone.getText().trim().isEmpty()
                || txtAddress.getText().trim().isEmpty()
                || txtDistrict.getText().trim().isEmpty()
                || txtSector.getText().trim().isEmpty()
                || txtCell.getText().trim().isEmpty()
                || txtCapacity.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields!",
                    "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        try {
            int capacity = Integer.parseInt(txtCapacity.getText().trim());
            if (capacity <= 0) {
                JOptionPane.showMessageDialog(this, "Capacity must be greater than 0!",
                        "Validation Error", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Capacity must be a valid number!",
                    "Validation Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }

        return true;
    }

