    private void loadChurches() {
        if (tableModel == null) {
            tableModel = (DefaultTableModel) churchTable.getModel();
        }

        tableModel.setRowCount(0);
        try {
            churchService = rccms.client.util.RMIClientUtil.getChurchService();
            if (churchService != null) {
                List<Church> churches = churchService.findAll();
                for (Church church : churches) {
                    Object[] row = {
                        church.getChurchId(),
                        church.getChurchName(),
                        church.getRegistrationNumber(),
                        church.getPastorName(),
                        church.getPastorPhone(),
                        church.getDistrict(),
                        church.getCapacity(),
                        church.getBuildingType(),
                        church.getComplianceStatus()
                    };
                    tableModel.addRow(row);
                }
            } else {
                 JOptionPane.showMessageDialog(this, "Could not connect to server.", "Connection Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading churches: " + e.getMessage());
        }
    }

