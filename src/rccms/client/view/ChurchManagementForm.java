    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        // Check if Church ID field has a value
        if (txtChurchID.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please search for or select a church to delete!",
                    "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int option = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this church?",
                "Confirm Delete", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.YES_OPTION) {
            try {
                // Get Church ID from the text field instead of table selection
                int churchId = Integer.parseInt(txtChurchID.getText().trim());

                churchService = rccms.client.util.RMIClientUtil.getChurchService();
                if (churchService.delete(churchId)) {
                    JOptionPane.showMessageDialog(this, "Church deleted successfully!",
                            "Success", JOptionPane.INFORMATION_MESSAGE);
                    loadChurches();
                    clearFields();
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to delete church!",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Invalid Church ID!",
                        "Error", JOptionPane.ERROR_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
                 JOptionPane.showMessageDialog(this, "Error deleting church: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        // Check if Church ID field has a value
        if (txtChurchID.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please search for or select a church to update!",
                    "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!validateInputs()) {
            return;
        }

        try {
            // Get Church ID from the text field instead of table selection
            int churchId = Integer.parseInt(txtChurchID.getText().trim());
            churchService = rccms.client.util.RMIClientUtil.getChurchService();
            Church church = churchService.findById(churchId);

            if (church == null) {
                JOptionPane.showMessageDialog(this, "Church not found!",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            church.setChurchName(txtChurchName.getText().trim());
            church.setRegistrationNumber(txtRegistrationNumber.getText().trim());
            church.setPastorName(txtPastorName.getText().trim());
            church.setPastorEmail(txtPastorEmail.getText().trim());
            church.setPastorPhone(txtPastorPhone.getText().trim());
            church.setAddress(txtAddress.getText().trim());
            church.setDistrict(txtDistrict.getText().trim());
            church.setSector(txtSector.getText().trim());
            church.setCell(txtCell.getText().trim());
            church.setCapacity(Integer.parseInt(txtCapacity.getText().trim()));
            church.setBuildingType(cmbBuildingType.getSelectedItem().toString());

            if (churchService.update(church)) {
                JOptionPane.showMessageDialog(this, "Church updated successfully!",
                        "Success", JOptionPane.INFORMATION_MESSAGE);
                loadChurches();
                clearFields();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update church!",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid Church ID or capacity value!",
                    "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error updating church: " + e.getMessage());
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

