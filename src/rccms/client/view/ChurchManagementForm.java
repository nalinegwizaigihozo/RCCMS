    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed

        String idText = txtSearch.getText().trim();

        if (idText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Church ID");
            return;
        }

        try {
            int churchId = Integer.parseInt(idText);

            // Call the service to find the church
            churchService = rccms.client.util.RMIClientUtil.getChurchService();
            Church church = churchService.findById(churchId);

            if (church != null) {
                // Populate church details in the form fields
                populateChurchDetails(church);

                // Enable update and delete buttons
                btnUpdate.setEnabled(true);
                btnDelete.setEnabled(true);

                JOptionPane.showMessageDialog(this, "Church found! You can now update or delete the record.");

            } else {
                JOptionPane.showMessageDialog(this, "No church found with ID: " + churchId);
                clearFields(); // Use clearFields instead of clearDetailFields
                btnUpdate.setEnabled(false);
                btnDelete.setEnabled(false);
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid ID. Only numbers allowed.");
            clearFields(); // Use clearFields instead of clearDetailFields
            btnUpdate.setEnabled(false);
            btnDelete.setEnabled(false);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error finding church: " + e.getMessage());
        }

    }

